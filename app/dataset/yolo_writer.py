#!/usr/bin/env python3
# -*- coding: utf-8 -*-
"""
YOLO format writer (Ultralytics-compatible).

YOLO label format (one .txt per image):
    <class_id> <x_center> <y_center> <width> <height>
all values normalized to [0, 1] relative to image size.
"""

import json
import os
from typing import List, Tuple


def write_yolo_labels(label_dir: str, image_filename: str,
                       image_size: Tuple[int, int],
                       boxes: List[Tuple[int, float, float, float, float]]):
    """
    boxes: list of (class_id, x_center_px, y_center_px, w_px, h_px).
    image_size: (width, height) in pixels.
    """
    img_w, img_h = image_size
    if img_w <= 0 or img_h <= 0:
        return
    base = os.path.splitext(image_filename)[0]
    txt_path = os.path.join(label_dir, base + ".txt")
    lines = []
    for class_id, xc, yc, w, h in boxes:
        lines.append("%d %.6f %.6f %.6f %.6f" % (
            int(class_id),
            float(xc) / img_w,
            float(yc) / img_h,
            float(w)  / img_w,
            float(h)  / img_h,
        ))
    with open(txt_path, "w", encoding="utf-8") as f:
        f.write("\n".join(lines))


def write_classes_txt(path: str, class_names: List[str]):
    with open(path, "w", encoding="utf-8") as f:
        f.write("\n".join(class_names) + "\n")


def write_data_yaml(path: str, dataset_root: str, class_names: List[str],
                     train_subdir: str = "images/train",
                     val_subdir: str = "images/val"):
    """Ultralytics YOLOv8 data.yaml 형식."""
    lines = [
        "# Auto-generated by RAIM Scope",
        "path: %s" % dataset_root,
        "train: %s" % train_subdir,
        "val: %s" % val_subdir,
        "",
        "nc: %d" % len(class_names),
        "names:",
    ]
    for i, name in enumerate(class_names):
        lines.append("  %d: %s" % (i, name))
    with open(path, "w", encoding="utf-8") as f:
        f.write("\n".join(lines) + "\n")


def coco_to_yolo(coco_json_path: str, output_dir: str,
                  image_source_dir: str = None,
                  val_ratio: float = 0.2,
                  seed: int = 42) -> dict:
    """
    COCO JSON → YOLO 형식 변환 + 무작위 train/val 분할.

    output_dir/
      ├── images/{train, val}/    이미지 80/20 무작위 분할
      ├── labels/{train, val}/    각 이미지에 대응하는 .txt
      ├── classes.txt
      └── data.yaml

    val_ratio: 0.0 ~ 0.5  (0이면 모두 train, 0.2 = 20% val)
    seed: 무작위성 재현 (같은 dataset → 같은 분할)
    """
    import random
    import shutil

    with open(coco_json_path, "r", encoding="utf-8") as f:
        data = json.load(f)

    classes = sorted(data.get("categories", []), key=lambda c: c["id"])
    class_names = [c["name"] for c in classes]

    images_train = os.path.join(output_dir, "images", "train")
    labels_train = os.path.join(output_dir, "labels", "train")
    images_val   = os.path.join(output_dir, "images", "val")
    labels_val   = os.path.join(output_dir, "labels", "val")
    for d in (images_train, labels_train, images_val, labels_val):
        os.makedirs(d, exist_ok=True)

    write_classes_txt(os.path.join(output_dir, "classes.txt"), class_names)
    write_data_yaml(os.path.join(output_dir, "data.yaml"),
                     output_dir, class_names)

    ann_by_img = {}
    for ann in data.get("annotations", []):
        ann_by_img.setdefault(ann["image_id"], []).append(ann)

    images = list(data.get("images", []))
    n_total = len(images)

    # Train/val 무작위 분할 (deterministic — seed 고정)
    rng = random.Random(seed)
    indices = list(range(n_total))
    rng.shuffle(indices)
    if n_total <= 1:
        n_val = 0  # 1장만 있으면 모두 train
    else:
        n_val = max(1, int(round(n_total * val_ratio)))
        # train도 최소 1장 보장
        n_val = min(n_val, n_total - 1)
    val_set = set(indices[:n_val])

    n_train_imgs = 0
    n_val_imgs = 0
    n_annotations = 0

    for i, img_info in enumerate(images):
        fname = img_info["file_name"]
        w = img_info["width"]
        h = img_info["height"]

        is_val = i in val_set
        target_img_dir = images_val if is_val else images_train
        target_lbl_dir = labels_val if is_val else labels_train

        if image_source_dir:
            src = os.path.join(image_source_dir, fname)
            dst = os.path.join(target_img_dir, fname)
            if os.path.isfile(src) and not os.path.exists(dst):
                shutil.copy2(src, dst)

        boxes = []
        for ann in ann_by_img.get(img_info["id"], []):
            x, y, bw, bh = ann["bbox"]
            xc = x + bw / 2.0
            yc = y + bh / 2.0
            boxes.append((ann["category_id"], xc, yc, bw, bh))
            n_annotations += 1

        write_yolo_labels(target_lbl_dir, fname, (w, h), boxes)

        if is_val:
            n_val_imgs += 1
        else:
            n_train_imgs += 1

    return {
        "n_images": n_total,
        "n_train": n_train_imgs,
        "n_val": n_val_imgs,
        "n_annotations": n_annotations,
        "n_classes": len(class_names),
        "val_ratio": val_ratio,
        "output_dir": output_dir,
    }
